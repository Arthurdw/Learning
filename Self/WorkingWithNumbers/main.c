#include <stdio.h>
#include <math.h>

int main() {
    // Some basic math operations:
    printf("%f\n", 1.2 + 2.3);
    printf("%f\n", 1.2 - 2.3);
    printf("%f\n", 1.2 * 2.3);
    printf("%f\n", 1.2 / 2.3);

    // Working with functions:
    printf("%d\n", (int) pow(10, 2));
    printf("%d\n", (int) sqrt(36));
    printf("%d\n", (int) ceil(123.123));
    printf("%d", (int) floor(123.123));

    return 0;
}
